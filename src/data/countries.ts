export const getCountryByDialCode = (dialCode: string): Country | undefined => {
  return countries.find(c => c.dialCode === dialCode);
};

export const getCountryByCode = (code: string): Country | undefined => {
  return countries.find(c => c.code === code);
};

export const validatePhoneNumber = (phoneNumber: string, country: Country): { valid: boolean; message: string } => {
  const digitsOnly = phoneNumber.replace(/\D/g, '');
  
  if (digitsOnly.length < country.minLength) {
    return { 
      valid: false, 
      message: `Phone number for ${country.name} must have at least ${country.minLength} digits` 
    };
  }
  
  if (digitsOnly.length > country.maxLength) {
    return { 
      valid: false, 
      message: `Phone number for ${country.name} must have at most ${country.maxLength} digits` 
    };
  }
  
  return { valid: true, message: '' };
};
